<script>
export default {
  data() {
    return {
      titleOrEjari: 'ejari',
      currentStep: 1,
      address: '',
      selectedEmirate: '',
      hasEjari: '',
      ejariNumber: '',
      ejariIssueDate: '',
      ejariExpiryDate: '',
      ejariFile: null,
      titleDeed: '',
      dewaAccountNumber: '',
      dewaPremisesNumber: '',
      dewaFile: null,
      emirates: [
        'Dubai',
        'Sharjah',
        'RAK',
        'AUH',
        'Umm Al Quwain',
        'Fujairah',
        'Al Ain',
        'I do not reside in the UAE',
      ],
      showStepper: {
        0: true,
        1: false,
        2: false,
        3: false,
        4: false,
        5: false,
      },
      loaders: {
        isInitializing: true,
        isSubmitting: false,
      },
      isFormSubmitSuccessful: false,
      headerString: '',
      isErrorExists: false,
      showAuthModal: false,
      authErrorMessage: '',
      showConfirmationModal: false,
      isAuthenticated: true, // Default to true, will be set to false on auth failure
      snackOptions: {
        color: 'green',
        text: '',
        show: false,
      },
      user_name: '',
      userId: "",
      rules: [(v) => !!v || 'This field is required'],
    }
  },
  methods: {
    goToNextStep(ref = null) {
      console.log("ref and the boolean versions is -->", this.$refs[ref], ref, ref && !this.validateInputs())

      if(ref && !this.validateInputs(ref)) return

      if (this.currentStep === 1) {
        if (this.selectedEmirate === 'Dubai') {
          this.showStepper[2] = true
          // Wait for DOM update before moving to the next step
          this.$nextTick(() => {
            this.currentStep = 2
          })
        } else if (this.selectedEmirate === 'I do not reside in the UAE') {
          this.showStepper[5] = true
          this.$nextTick(() => {
            this.currentStep = 5 // Skip to the Thank You message
          })
        } else {
          this.showStepper[4] = this.showStepper[5] = true
          this.$nextTick(() => {
            this.currentStep = 4 // Skip to the Address info
          })
        }
      } else if (this.currentStep === 2) {
        this.showStepper[4] = this.showStepper[5] = true
        if (this.hasEjari === 'No') {
          this.$nextTick(() => {
            this.currentStep = 4 // Skip to the Address info
          })
        } else {
          this.showStepper[3] = true
          this.$nextTick(() => {
            this.currentStep = 3
          })
        }
      } else {
        this.showStepper[this.currentStep + 1] = true
        this.$nextTick(() => {
          this.currentStep++
        })
      }

    },
    async setAuthToken() {
      if (this.$route?.query?.token) {
        this.headerString = 'Bearer '.concat(this.$route?.query?.token)
        this.loaders.isInitializing = false
        // Get user details after setting auth token
        await this.getUserDetails()
      } else {
        this.isErrorExists = true
      }
    },

    async getUserDetails() {
      try {
        const response = await this.$axios.get(`users/get-authenticated-user`, {
          headers: { Authorization: this.headerString },
        })
        
        // Authentication successful, ensure flag is set to true
        this.isAuthenticated = true;
        
        // Format the user's name properly with capitalization
        const firstName = response?.data?.first_name || '';
        const lastName = response?.data?.last_name || '';
        const userId = response?.data?._id || '';
        
        if (firstName || lastName) {
          this.user_name = `${firstName} ${lastName}`.trim();
          this.userId = userId;
          console.log('User authenticated successfully:', this.user_name, 'and id', this.userId);
          
        } else {
          // If we get a response but no name, set a generic greeting
          this.user_name = 'User';
          console.log('User authenticated but name not found in response data');
        }
      } catch (e) {
        console.error('Error fetching user details:', e)
        
        // Set authentication flag to false
        this.isAuthenticated = false;
        
        // Determine specific error message based on error
        if (e.response) {
          if (e.response.status === 401 || e.response.status === 403) {
            this.authErrorMessage = "Your session has expired or you don't have permission. Please request a new link to continue.";
          } else if (e.response.status >= 500) {
            this.authErrorMessage = "Your token must have expired!. Please try again later or contact support.";
          } else {
            this.authErrorMessage = "Unable to retrieve your information. Please request a new link to continue.";
          }
        } else if (e.request) {
          this.authErrorMessage = "Network error. Please check your connection and try again.";
        } else {
          this.authErrorMessage = "Your session has expired or is invalid. Please request a new link to continue.";
        }
        
        this.showAuthModal = true
        this.loaders.isInitializing = false
      }
    },

    async uploadFiles(files) {
      let response = []
      
      try {
        for (const [index, file] of files.entries()) {
          const fd = new FormData()
          fd.append('documents', file);
          fd.append("userId", this.userId)
  
          const doc = await this.$axios.$post(
            `/documents/simpleupload`,
            fd,
             {
            headers: { Authorization: this.headerString },
          }
          )
  
          response[index] = doc?.[0]
        }
        return response
      } catch (e) {
        // Check if this is an authentication error
        if (e.response && (e.response.status === 401 || e.response.status === 403)) {
          console.error('Authentication error during file upload:', e);
          this.isAuthenticated = false;
          this.authErrorMessage = "Your session has expired. Please request a new link to continue.";
          this.showAuthModal = true;
          throw e; // Rethrow to be handled by the calling function
        }
        throw e; // Rethrow other errors
      }
    },

    validateInputs(ref) {
      let valid = true
      if (this.$refs[ref] && !this.$refs[ref].validate()) {
        this.snackOptions.color = 'red'
        this.snackOptions.text = 'Kindly correct the errors on the form fields'
        this.snackOptions.show = true
        valid = false
      }
      return valid
    },

    async submit() {
      // Check authentication status first
      if (!this.isAuthenticated) {
        this.showAuthModal = true;
        return;
      }

      if (this.selectedEmirate === 'I do not reside in the UAE') return

      this.loaders.isSubmitting = true
      let files = [];
      files = this.ejariFile && this.dewaFile ? [this.ejariFile, this.dewaFile] : []
      //construct the request body
      const form = {
        emirate: this.selectedEmirate,
        tenancyResidence: this.hasEjari,
        address: this.address,
      }

      try {
        // append ejari and dewa info if user chose yes in the tenancy question
        if(this.hasEjari === 'Yes') {
          if(this.titleOrEjari == 'ejari') {
            form.ejari = {
              contractNumber: this.ejariNumber,
              issueDate: this.ejariIssueDate,
              expirationDate: this.ejariExpiryDate,
            }
          }
          else if(this.titleOrEjari == 'title') {
            form.titleDeed = {
              titleDeed: this.titleDeed,
            }
          }
          // form.ejari = {
          //   contractNumber: this.ejariNumber,
          //   issueDate: this.ejariIssueDate,
          //   expirationDate: this.ejariExpiryDate,
          // }
          form.dewa = {
            accountNumber: this.dewaAccountNumber,
            premisesNumber: this.dewaPremisesNumber,
            titleDeed: this.titleDeed,
          }
        }
        
        //check if any files were uploaded
        if (files.length > 0) {
          // get links from uploaded files first
          var res = await this.uploadFiles(files)
          //add these links to the request body
          if(this.titleOrEjari == 'ejari') form.ejari.attachment = res[0]
          if(this.titleOrEjari == 'title') form.titleDeed.attachment = res[0]
          form.dewa.attachment = res[1]
        }

        //default tenancyResidence to false if not provided
        form.tenancyResidence = form.tenancyResidence || "No"
        console.log("form is -->", form)

        const submitResponse = await this.$axios.post(
          `/users/update/tenancy-and-residence-details`,
          form,
          {
            headers: { Authorization: this.headerString },
          }
        )

        // Mark the submission as successful
        this.isFormSubmitSuccessful = true
        
        // Display success message in snackbar
        this.snackOptions.color = 'green'
        this.snackOptions.text = 'Update Successful!'
        this.snackOptions.show = true
        
        // Show confirmation modal with a small delay for better UX
        setTimeout(() => {
          this.showConfirmationModal = true
        }, 300)
      } catch (e) {
        if (e.response) {
          if (e.response.status === 401 || e.response.status === 403) {
            // Authentication or authorization error
            this.authErrorMessage = "Your session has expired. Please request a new link to continue."
            this.showAuthModal = true
          } else if (e.response.status >= 500) {
            // Server error
            this.snackOptions.color = 'red'
            this.snackOptions.text = 'Server error occurred. Please try again later or contact support.'
            this.snackOptions.show = true
          } else if (e.response.status === 422 || e.response.status === 400) {
            // Validation error
            this.snackOptions.color = 'red'
            this.snackOptions.text = e.response.data?.message || 'There was a problem with the data you submitted. Please check and try again.'
            this.snackOptions.show = true
          } else {
            // General error
            this.snackOptions.color = 'red'
            this.snackOptions.text = 'An error occurred while submitting the form values. Please try again or refresh the page.'
            this.snackOptions.show = true
          }
        } else if (e.request) {
          // Network error
          this.snackOptions.color = 'red'
          this.snackOptions.text = 'Network error occurred. Please check your connection and try again.'
          this.snackOptions.show = true
        } else {
          // Unknown error
          this.snackOptions.color = 'red'
          this.snackOptions.text = 'An unexpected error occurred. Please refresh the page and try again.'
          this.snackOptions.show = true
        }
        
        console.error('Form submission error:', e)
      }
      this.loaders.isSubmitting = false
    },
  },
  async mounted() {
    await this.setAuthToken()
  },
}
