                                    <v-list-item-title>
                                      {{ data.item.emoji }} {{ data.item.name }}
                                    </v-list-item-title>
                                  </v-list-item-content>
                                  <v-list-item-action>
                                    +{{ data.item.code }}
                                  </v-list-item-action>
                                </v-list-item>
                              </template>
                            </v-select>
                          </template>
                          <v-text-field
                            v-model="form.emergency.other_phone"
                            label="Other Phone"
                            @input="enforceValidInputIfProvided('emergency_other_phone')"
                            :rules="optional_phone_rule['emergency_other_phone']"
                          ></v-text-field>
                        </v-input>
                      </v-col>
                    </v-row>
                  </v-card-text>
                </v-card>

                <v-card-actions class="pb-9 vs_custom d-flex px-9">
                  <v-spacer></v-spacer>
                  <v-btn
                    class="ma-2 px-4 full-rounded"
                    color="primary"
                    @click="submit"
                    :loading="isSubmitting"
                    dark
                    style="border-radius: 12rem !important"
                    elevation="0"
                  >
                    <v-icon left> content_save_outline </v-icon>
                    SUBMIT
                  </v-btn>
                </v-card-actions>
              </v-card>
            </div>
          </v-form>
        </transition-group>

        <template v-if="isErrorExists">
          <div class="pa-9">
            <h1>Hello there,</h1>
            <p>
              An error occured while initializing the form. Please try again.
              <br />If this problem persists, kindly request a new link from
              <a href="mailto:support@nathandigital.com"
                >support@nathandigital.com.</a
              >
            </p>
          </div>
        </template>

        <template v-if="isFormSubmitSuccessful">
          <div class="pa-9">
            <h1>Thank you {{ user_name }},</h1>
            <p>
              We appreciate you for taking time to submit your information
              details. Should there be any issue, we will communicate to you via
              email.
            </p>
          </div>
        </template>
      </v-card>
    </v-container>

    <!-- snackbar -->
    <v-snackbar
      v-model="snackOptions.show"
      :timeout="3000"
      top
      :color="snackOptions.color"
    >
      {{ snackOptions.text }}

      <template v-slot:action="{ attrs }">
        <v-btn v-bind="attrs" text @click="snackbar.show = false">Close</v-btn>
      </template>
    </v-snackbar>
  </v-container>
</template>

<script>
import { countries } from 'countries-list'
const _main_rule = [(v) => !!v || 'This field is required']
const _phone_rule = [
  ..._main_rule,
  (v) => /^[0-9]+$/.test(v) || 'Please input a number',
]

export default {
  data() {
    return {
      valid: false,
      headerString: '',
      form: {
        personal: {
          personal_email: '',
          gender: '',
          address: '',
          marital_status: '',
        },
        emergency: {
          name: '',
          relationship: '',
          email: '',
          phone: '',
          other_phone: '',
        },
        emergency_uae: {
          name: '',
          relationship: '',
          email: '',
          phone: '',
          other_phone: '',
        },
        phone: '',
        other_phone: '',
      },
      selectedCountry: {
        phone: {
          code: '971',
          emoji: '🇦🇪',
          name: 'United Arab Emirates',
        },
        other_phone: null,
        emergency: null,
        emergency_other_phone: null,
        emergency_uae: null,
        emergency_uae_other_phone: null,
      },
      countries: [],
      marital_status_options: [
        'Single',
        'Married',
        'Divorced',
        'Widowed',
        'Single (With Children)',
      ],
      gender_options: ['Male', 'Female'],
      main_rule: _main_rule,
      phone_rule: _phone_rule,
      optional_phone_rule: {
        'other_phone': [],
        'emergency_uae_other_phone': [],
        'emergency_other_phone': [],
      },
      optional_phone_code_rule: {
        'other_phone': [],
        'emergency_uae_other_phone': [],
        'emergency_other_phone': [],
      },
      phoneFields: [
        {
          selectModel: 'phone',
          formField: 'phone',
        },
        {
          selectModel: 'other_phone',
          formField: 'other_phone',
        },
        { 
          selectModel: 'emergency', 
          formField: 'emergency.phone' 
        },
        {
          selectModel: 'emergency_other_phone',
          formField: 'emergency.other_phone',
        },
        { 
          selectModel: 'emergency_uae', 
          formField: 'emergency_uae.phone' 
        },
        {
          selectModel: 'emergency_uae_other_phone',
          formField: 'emergency_uae.other_phone',
        },
      ],
      debounceAction: null,
      email_rule: [
        ..._main_rule,
        (v) => /.+@.+\..+/.test(v) || 'Email must be valid',
        (v) => v === v.toLowerCase() || 'Email must be in lowercase',
      ],
      uae_phone_rule: [
        ..._main_rule,
        (v) =>
          /^5[0-9]{8}$/.test(v) ||
          'Please input a valid UAE number(example, Number: 5xxxxxxxx) Please note that country code is already locked to +971',
      ],
      isLoadingFormValues: true,
      isErrorExists: false,
      isSubmitting: false,
      isFormSubmitSuccessful: false,
      snackOptions: {
        text: '',
        color: '',
        show: false,
      },
      user_name: '',
    }
  },
  methods: {
    async setAuthToken() {
      if (this.$route?.query?.token) {
        this.headerString = 'Bearer '.concat(this.$route?.query?.token)
      } else {
        this.isErrorExists = true
      }
    },

    set_countries(uae = false) {
      const countryCodes = Object.keys(countries)
      // "form\.([a-z_.]*phone)
      const country_list = countryCodes.map((code) => ({
        name: countries[code].name,
        code: countries[code].phone,
        emoji: countries[code].emoji,
      }))
      this.countries = country_list
    },

    async getUserDetails() {
      try {
        const response = await this.$axios.get(`users/get-authenticated-user`, {
          headers: { Authorization: this.headerString },
        })
        this.user_name = `${response?.data?.first_name} ${response?.data?.last_name}`
        this.form = this.mergeFormData(this.form, response.data)

        this.setPhoneCountryCodes(response.data)
      } catch (e) {
        this.isErrorExists = true
      }
      this.isLoadingFormValues = false
    },

    mergeFormData(form, apiData) {
      for (let key in form) {
        if (
          form[key] &&
          typeof form[key] === 'object' &&
          !Array.isArray(form[key])
        ) {
          form[key] = this.mergeFormData(form[key], apiData[key] || {})
        } else {
          if (apiData[key] !== undefined && apiData[key] !== null) {
            //remove country code from the numbers
            if (
              (key.includes('phone') || key.includes('mobile')) &&
              apiData[key]?.includes('+')
            ) {
              form[key] = apiData[key].split(' ').slice(1).join(' ')
            } else {
              form[key] = apiData[key]
            }
          }
        }
      }

      return form
    },

    setPhoneCountryCodes(apiData) {

      // function to remove code from value and assign to form input
      const assignCodeToCodeModels = (phone_value_array, selectModel) => {
        //country code cannot be more than 4 digits including the plus, so length 5
        if (phone_value_array[0].length > 5) {
          return
        }

        const countryCode = phone_value_array[0].replace('+', '')
        for (let country of this.countries) {
          if (country.code === countryCode) {
            this.selectedCountry[selectModel] = country
          }
        }
      }

      this.phoneFields.forEach(({ selectModel, formField }) => {
        const split_key = formField.split('.')

        if (split_key[1]) {
          //phone value does not have the leading +
          if (!apiData[split_key[0]][split_key[1]]?.includes('+')) {
            return
          }

          const phone_value_array =
            apiData[split_key[0]][split_key[1]].split(' ')
          if (!phone_value_array) return

          assignCodeToCodeModels(phone_value_array, selectModel)
        } else {
          const phone_value_array = apiData[split_key[0]]?.split(' ')

          //avoid replacing phone property as it is locked to UAE code only
          if (!phone_value_array || split_key[0] === 'phone') return

          assignCodeToCodeModels(phone_value_array, selectModel)
        }
      })
    },

    async submit() {
      if (!this.$refs.form.validate()) {
        this.snackOptions.color = 'red'
        this.snackOptions.text = 'Kindly correct the errors on the form fields'
        this.snackOptions.show = true
        return
      }
      this.isSubmitting = true

      // Prepend country codes to phone numbers
      this.form.phone = this.concatCountryCode(
        this.selectedCountry.phone,
        this.form.phone
      )
      this.form.other_phone = this.concatCountryCode(
        this.selectedCountry.other_phone,
        this.form.other_phone
      )
      this.form.emergency.phone = this.concatCountryCode(
        this.selectedCountry.emergency,
        this.form.emergency.other_phone
      )
      this.form.emergency.other_phone = this.concatCountryCode(
        this.selectedCountry.emergency_other_phone,
        this.form.emergency.other_phone
      )
      this.form.emergency_uae.phone = this.concatCountryCode(
        this.selectedCountry.emergency_uae,
        this.form.emergency_uae.phone
      )
      this.form.emergency_uae.other_phone = this.concatCountryCode(
        this.selectedCountry.emergency_uae_other_phone,
        this.form.emergency_uae.other_phone
      )
      console.log('+++++=form ', JSON.parse(JSON.stringify(this.form)))

      try {
        await this.$axios.post(`/users/update/missing-details`, this.form, {
          headers: { Authorization: this.headerString },
        })
        this.isFormSubmitSuccessful = true
        this.snackOptions.color = 'green'
        this.snackOptions.text = 'Update Successful!'
        this.snackOptions.show = true
      } catch (e) {
        this.snackOptions.color = 'red'
        this.snackOptions.text =
          'An error occurred while submitting the form values. Please try again.'
        this.snackOptions.show = true
      }
      this.isFormSubmitSuccessful = true
      this.isSubmitting = false
    },

    concatCountryCode(country, phone) {
      if (country && country.code && phone) {
        return `+${country.code} ${phone}`
      }
      return phone
    },

    updatePhone(field, event) {
      this.selectedCountry[field] = event
    },

    closeSelectMenu(selectModel) {
      // Access the v-select component via ref and close its menu
      if (this.$refs[selectModel]) {
        this.$refs[selectModel].isMenuActive = false // This should close the menu
      }
    },

    enforceValidInputIfProvided(model) {
      if (this.debounceAction) clearTimeout(this.debounceAction)
      this.debounceAction = setTimeout(() => {

        const formField = this.phoneFields.find(item => item.selectModel === model)?.formField
        
        if(!formField) return
        
        const split = formField.split('.')
        console.log('split', split)
        let inputValue = ''
        if(split.length > 1) {
          inputValue = this.form[split[0]][split[1]]
        }else {
          inputValue = this.form[split[0]]
        }

        //enforce the rules only if the user decides to type to the other phone
        if(inputValue) {
          this.optional_phone_rule[model] = [_phone_rule[1]]
          this.optional_phone_code_rule[model] = _main_rule
        }else {
          this.optional_phone_rule[model] = []
          this.optional_phone_code_rule[model] = []
        }
      },500)
    }
  },
  async mounted() {
    // await this.$auth.logout()
    this.set_countries()
    await this.setAuthToken()
    await this.getUserDetails()
  },
  
  watch: {
    'selectedCountry.phone'() {
      this.closeSelectMenu('phone');
    },
    'selectedCountry.other_phone'() {
      this.closeSelectMenu('other_phone');
    },
    'selectedCountry.emergency'() {
      this.closeSelectMenu('emergency');
    },
    'selectedCountry.emergency_other_phone'() {
      this.closeSelectMenu('emergency_other_phone');
    },
    'selectedCountry.emergency_uae'() {
      this.closeSelectMenu('emergency_uae');
    },
    'selectedCountry.emergency_uae_other_phone'() {
      this.closeSelectMenu('emergency_uae_other_phone');
    },
  }
}
</script>

